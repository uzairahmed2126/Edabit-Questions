// Pair Management
// Given two arguments, return an array which contains these two arguments.

// Examples
function makePair(...value) {
  return value;
}
console.log(makePair(1, 2)); //➞ [1, 2]
console.log(makePair(51, 21)); //➞ [51, 21]
console.log(makePair(512124, 215)); //➞ [512124, 215]
